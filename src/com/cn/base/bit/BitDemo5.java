public class BitDemo5 {
    public static void main(String[] args)
    {
        int a=22;
        int b=5;
        double c=5;
        
        System.out.println(b+"+"+c+"="+(b+c));
        System.out.println(b+"-"+c+"="+(b-c));
        System.out.println(b+"*"+c+"="+(b*c));
        System.out.println(a+"/"+b+"="+(a/b));
        System.out.println(a+"%"+b+"="+(a%b));
        System.out.println(a+"/"+c+"="+(a/c));
        System.out.println(a+"%"+c+"="+(a%c));
    }
}
